                        <h3>Pick a time zone, any time zone.</h3>
                        <h4>Calgary - University of Calgary - April 27-28 | Toronto - University of Toronto - April 28-29</h4>
                       CUTC's national conference has always been the cr&egrave;me de la cr&egrave;me of events. But why have one when you can have TWO! That's right, national conference will not be in just one city but two cities this year. Travelling out West for the CUTC Tour has got us so excited, we knew we had to have a conference there as well.  <br/><br/>
    The national conference will take place in Calgary and Toronto, giving our delegates a choice of participating in the beautiful Canadian West or the urban beauty of Toronto. Both conferences will have everything CUTC: hundreds of hours of incredible speakers, events, and workshops packed into two breakneck days.
