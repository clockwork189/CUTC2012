                        <h3>We change how you think about technology.</h3><br/>
                       We'll keep the <em>what</em> short. We're a group of students who create the Canadian Undergraduate Technology Conference, every year since 1999. That means bringing everything (and everyone) tech-related together to discuss, share, and ultimately change your perspective on technology. Our initiatives include one-day, local events, as well a massive conference spanning the entire country.<br/><br/>
    Now, we think it's more important that you understand  <em>why</em>. <br/><br/>
                       Like that temperamental, crazy hippie Steve Jobs said, "The only way to be truly satisfied is to do what you believe is great work. And the only way to do great work is to love what you do." He may have been insane, but he made a dent in the universe. We're hoping a weekend at CUTC will give you the inspiration, the passion, the perspective and the tools to do the same. <em>That's</em> what we're <em>really</em> about. You.<br/><br/>
                       Beyond the amazing speakers and boundary-pushing companies, we'll bring in hundreds of your peers with whom you will share a few beers, kick around some ideas, and spark a few mutual dreams. You might just join the ranks of some of the greatest relationships in history: Jobs and Wozniak, Buckingham and Nicks, Batman and Robin. <br/><br/>
                       All you have to do is buy a ticket, and take a chance. We'll take care of the rest. <br/><br/>
                       We do it for love. You should too. <br/><br/>
                       <h3>And if you get a chance, check out what we were up to at <a href="http://www.dev.cutc.ca/cutc2011" target="_blank" style="color:#be1212; ">last year's conference</a></h3>
